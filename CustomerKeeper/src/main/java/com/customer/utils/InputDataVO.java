public class InputDataVO {
    public String userId;
    public String optType;
    public String optObject;
    public String optData;


    public String getUserId() {

        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getOptType() {
        return optType;
    }

    public void setOptType(String optType) {
        this.optType = optType;
    }

    public String getOptObject() {
        return optObject;
    }

    public void setOptObject(String optObject) {
        this.optObject = optObject;
    }

    public String getOptData() {
        return optData;
    }

    public void setOptData(String optData) {
        this.optData = optData;
    }
}
